const moveMap: { [index: string]: { [index: string]: number } } = {
  // Rock
  A: { X: 3, Y: 6, Z: 0 },
  // Paper
  B: { X: 0, Y: 3, Z: 6 },
  // Scissors
  C: { X: 6, Y: 0, Z: 3 },
};

const movePoints: { [index: string]: number } = {
  // Rock
  X: 1,
  // Paper
  Y: 2,
  // Scissors
  Z: 3,
};

type State = "win" | "lose" | "draw";
interface NeededStateMap {
  [index: string]: { state: State; points: number };
}
interface MoveNeededMap {
  [index: string]: { [key in State]: string };
}

const neededState: NeededStateMap = {
  X: { state: "lose", points: 0 },
  Y: { state: "draw", points: 3 },
  Z: { state: "win", points: 6 },
};

const moveNeeded: MoveNeededMap = {
  // Rock
  A: { win: "Y", lose: "Z", draw: "X" },
  // Paper
  B: { win: "Z", lose: "X", draw: "Y" },
  // Scissors
  C: { win: "X", lose: "Y", draw: "Z" },
};

export const day2 = () => {
  console.log("****** Day 2 ******************");

  const rounds = readMoves(input);
  const result = rounds.reduce((acc, round) => {
    return acc + moveMap[round.opponent][round.me] + movePoints[round.me];
  }, 0);
  console.log("Part 1 - Total Score: " + result);

  const result2 = rounds.reduce((acc, round) => {
    const needed = neededState[round.me];
    return (
      acc + movePoints[moveNeeded[round.opponent][needed.state]] + needed.points
    );
  }, 0);
  console.log("Part 2 - Total Score: " + result2);
  console.log("*******************************");
};

const readMoves = (moves: string) => {
  return moves.split("\n").map((move) => {
    const round = move.split(" ");
    return { opponent: round[0], me: round[1] };
  });
};

