const part1 = () => {
  const visited = new Set<string>();

  const steps = input.split("\n").map((line) => {
    const [direction, distance] = line.split(" ");
    return { direction, distance: parseInt(distance, 10) };
  });

  let headPosition = { x: 0, y: 0 };
  let tailPosition = { x: 0, y: 0 };

  steps.forEach((step) => {
    for (let i = 0; i < step.distance; i++) {
      switch (step.direction) {
        case "R":
          headPosition.x += 1;
          break;
        case "U":
          headPosition.y += 1;
          break;
        case "L":
          headPosition.x -= 1;
          break;
        case "D":
          headPosition.y -= 1;
          break;
      }

      calculateNewPosition(headPosition, tailPosition);

      visited.add(`${tailPosition.x},${tailPosition.y}`);
    }
  });

  console.log("Part 1 - Visited: ", visited.size);
};

const part2 = () => {
  const visited = new Set<string>();

  const steps = input.split("\n").map((line) => {
    const [direction, distance] = line.split(" ");
    return { direction, distance: parseInt(distance, 10) };
  });

  const positions: { x: number; y: number }[] = new Array(10)
    .fill(0)
    .map(() => ({
      x: 0,
      y: 0,
    }));

  steps.forEach((step) => {
    for (let i = 0; i < step.distance; i++) {
      switch (step.direction) {
        case "R":
          positions[0].x += 1;
          break;
        case "U":
          positions[0].y += 1;
          break;
        case "L":
          positions[0].x -= 1;
          break;
        case "D":
          positions[0].y -= 1;
          break;
      }

      for (let j = 1; j < positions.length; j++) {
        positions[j] = calculateNewPosition(positions[j - 1], positions[j]);
      }

      visited.add(
        `${positions[positions.length - 1].x},${
          positions[positions.length - 1].y
        }`
      );
    }
  });
  console.log("Part 2 - Visited: ", visited.size);
};

function calculateNewPosition(
  head: { x: number; y: number },
  tail: { x: number; y: number }
) {
  const xDiff = difference(head.x, tail.x);
  const yDiff = difference(head.y, tail.y);

  if (xDiff === 0 && yDiff === 0) {
    return tail;
  }

  if ((xDiff > 1 || yDiff > 1) && head.x !== tail.x && head.y !== tail.y) {
    // upper right
    if (head.x > tail.x && head.y > tail.y) {
      tail.x += 1;
      tail.y += 1;
    }
    // upper left
    else if (head.x < tail.x && head.y > tail.y) {
      tail.x -= 1;
      tail.y += 1;
    }
    // lower left
    else if (head.x < tail.x && head.y < tail.y) {
      tail.x -= 1;
      tail.y -= 1;
    }
    // lower right
    else if (head.x > tail.x && head.y < tail.y) {
      tail.x += 1;
      tail.y -= 1;
    }
  } else if (xDiff > 1 && yDiff === 0) {
    if (head.x > tail.x) {
      tail.x += 1;
    } else {
      tail.x -= 1;
    }
  } else if (xDiff === 0 && yDiff > 1) {
    if (head.y > tail.y) {
      tail.y += 1;
    } else {
      tail.y -= 1;
    }
  }
  return tail;
}

function difference(a: number, b: number) {
  return Math.abs(a - b);
}

function printMap(positions: { x: number; y: number }[]) {
  const minX = Math.min(...positions.map((p) => p.x));
  const maxX = Math.max(...positions.map((p) => p.x));
  const minY = Math.min(...positions.map((p) => p.y));
  const maxY = Math.max(...positions.map((p) => p.y));

  const map = new Array(maxY - minY + 1)
    .fill(0)
    .map(() => new Array(maxX - minX + 1).fill("."));

  positions.forEach((p, i) => {
    map[p.y - minY][p.x - minX] = i === 0 ? "H" : i.toString();
  });

  console.log("");
  console.log(map.map((row) => row.join("")).join("\n"));
}

